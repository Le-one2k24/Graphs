#ifndef BORUVKA_H
#define BORUVKA_H

// Борувка (Boruvka) — минимальное остовное дерево для неориентированного взвешенного графа.
// Функции:
// int** boruvka_mst(int** adj, int** weight, int N, int& total_weight)
//   - adj: матрица смежности (0/1), неориентированный граф (симметричная матрица).
//   - weight: матрица весов (симметричная), 0 означает отсутствие ребра.
//   - N: число вершин.
//   - total_weight: выходной параметр — суммарный вес MST.
// Возвращает: указатель на новую матрицу смежности MST (N x N), или nullptr при ошибке.
// При успешном выполнении функция также сохраняет последний MST внутри модуля (get_last_mst).
int** boruvka_mst(int** adj, int** weight, int N, int& total_weight);

// Получить последний сохранённый MST (если был построен).
// Возвращает указатель на внутреннюю матрицу MST (не копию) и устанавливает N.
// Если MST отсутствует, возвращает nullptr и N=0.
int** get_last_mst(int& N);

// Очистить сохранённый последний MST.
void clear_last_mst();

#endif
