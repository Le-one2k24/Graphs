#ifndef EULER_H
#define EULER_H

#include <vector>
#include <utility>

// Проверка, является ли неориентированный граф (матрица смежности) эйлеровым.
// Граф считается связным (предполагается, что он уже связен).
// Для мультиграфа (возможны кратные рёбра) степень считается по всем рёбрам.
bool is_eulerian(int** adj, int N);

// Модифицирует граф (создаёт копию в виде матрицы с кратностями) так,
// чтобы он стал эйлеровым (добавляет минимальное количество рёбер между вершинами с нечётной степенью).
// Возвращает добавленные рёбра (пары вершин). Если граф уже эйлеров, возвращает пустой вектор.
// Исходная матрица adj не изменяется.
std::vector<std::pair<int,int>> make_eulerian(int** adj, int N, std::vector<std::vector<int>>& mult);

// Строит эйлеров цикл в эйлеровом мультиграфе, заданном матрицей кратностей mult.
// Возвращает последовательность вершин цикла (начинается и заканчивается одной вершиной).
// Предполагается, что граф связен и все степени чётные.
std::vector<int> find_eulerian_cycle(const std::vector<std::vector<int>>& mult, int N, int start = 0);

#endif